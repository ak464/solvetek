-- Function to get traffic sources
CREATE OR REPLACE FUNCTION public.get_traffic_sources(days_ago INT DEFAULT 30)
RETURNS TABLE (source TEXT, count BIGINT)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    RETURN QUERY
    SELECT 
        COALESCE(NULLIF(regexp_replace(referrer, '^https?://([^/]+).*', '\1'), ''), 'Direct / Unknown') AS source,
        COUNT(*) as count
    FROM public.analytics_events
    WHERE created_at > now() - (days_ago || ' days')::interval
    GROUP BY source
    ORDER BY count DESC
    LIMIT 10;
END;
$$;

-- Function to get device usage
CREATE OR REPLACE FUNCTION public.get_device_usage(days_ago INT DEFAULT 30)
RETURNS TABLE (device TEXT, count BIGINT)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    RETURN QUERY
    SELECT 
        COALESCE(device_type, 'desktop') AS device,
        COUNT(*) as count
    FROM public.analytics_events
    WHERE created_at > now() - (days_ago || ' days')::interval
    GROUP BY device
    ORDER BY count DESC;
END;
$$;
