-- Create a table for detailed analytics events
CREATE TABLE IF NOT EXISTS public.analytics_events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id UUID NOT NULL,
    path TEXT NOT NULL,
    referrer TEXT,
    user_agent TEXT,
    device_type TEXT, -- 'mobile', 'desktop', 'tablet', 'bot'
    country TEXT,     -- ISO code if available (optional for now)
    created_at TIMESTAMPTZ DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.analytics_events ENABLE ROW LEVEL SECURITY;

-- Allow anonymous inserts
CREATE POLICY "Allow anonymous analytics logging" 
ON public.analytics_events 
FOR INSERT 
WITH CHECK (true);

-- Allow admins to read all events
CREATE POLICY "Allow admins to read analytics" 
ON public.analytics_events 
FOR SELECT 
TO authenticated 
USING (
  EXISTS (
    SELECT 1 FROM public.profiles 
    WHERE profiles.id = auth.uid() AND profiles.role = 'admin'
  )
);

